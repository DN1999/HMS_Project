    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Code for upadate Room details
       String id = jTextField1.getText();
       String rnum = jTextField2.getText();
       String rtype = jComboBox2.getSelectedItem().toString();
       String rstatus = jComboBox3.getSelectedItem().toString();
       String check="";
        try{
          rs=st.executeQuery("select rid from room where rid='"+id+"' ");
          if(rs.next()==true){
             check=rs.getString(1);
          }
         if(check.equals(id)){
                if(id.equals("")||rnum.equals("")||rtype.equals("Select")||rstatus.equals("Select")){
                JOptionPane.showMessageDialog(this,"Some Field is Empty","",JOptionPane.INFORMATION_MESSAGE);         
             }else{          
               String sql = "update room set rid ='"+id+"', rnum='"+rnum+"', rtype='"+rtype+"',rstatus='"+rstatus+"' where rid='"+id+"'";
               PreparedStatement pst =con.prepareStatement(sql);
               pst.executeUpdate();
               JOptionPane.showMessageDialog(this,"Room Update Successfully","",JOptionPane.INFORMATION_MESSAGE);
               clearField();
             }
         }else{
            JOptionPane.showMessageDialog(this,"Room ID Not Exist for Update");   
         }
          }catch(Exception e){
      JOptionPane.showMessageDialog(this,e+"Room ID Already Exist");
    }
    }//GEN-LAST:event_jButton2ActionPerformed

