    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Code for delete
       String id = jTextField1.getText();
       String rnum = jTextField2.getText();
       String rtype = jComboBox2.getSelectedItem().toString();
       String rstatus = jComboBox3.getSelectedItem().toString();
       String check="";  
       try{
          rs=st.executeQuery("select * from room where rid='"+id+"'");
          if(rs.next()==true){
             check=rs.getString(1);
          } else{
            JOptionPane.showMessageDialog(this,"Room Details not found for delete","",JOptionPane.INFORMATION_MESSAGE);         
            }
          
         if(check.equals(id)){
         if(id.equals("")||rnum.equals("")||rtype.equals("Select")||rstatus.equals("Select")){
                JOptionPane.showMessageDialog(this,"Some Field is Empty","",JOptionPane.INFORMATION_MESSAGE);         
             }else{          
               int confirm = JOptionPane.showConfirmDialog(this,"Are You Really want to delete record","Confirm",JOptionPane.YES_NO_OPTION);
               if(confirm==0){
               String sql = "delete from room where rid='"+id+"'";
               PreparedStatement pst =con.prepareStatement(sql);
               pst.executeUpdate();
               JOptionPane.showMessageDialog(this,"Room Delete Successfully","",JOptionPane.INFORMATION_MESSAGE);
                clearField();
               }
             }
         }
          }catch(Exception e){
      JOptionPane.showMessageDialog(this,e+"Room ID Already Exist");
    }
    }//GEN-LAST:event_jButton3ActionPerformed

